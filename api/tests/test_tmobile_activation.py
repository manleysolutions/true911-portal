TOKEN_URL = "https://pit-oauth.t-mobile.com/oauth2/v2/tokens"
BASE_URL = "https://pit-apis.t-mobile.com"
# Default subscriber base path is the PIT gateway's /wholesale/v1/subscriber.
ACTIVATE_PATH = "/wholesale/v1/subscriber/activation"
ACTIVATE_URL = f"{BASE_URL}{ACTIVATE_PATH}"


@pytest.fixture
def tmobile_env(monkeypatch):
    """Generated RSA key + mocked T-Mobile settings (creds, hosts, activation env)."""
    key = rsa.generate_private_key(public_exponent=65537, key_size=2048)
    pem = key.private_bytes(
        encoding=serialization.Encoding.PEM,
        format=serialization.PrivateFormat.PKCS8,
        encryption_algorithm=serialization.NoEncryption(),
    ).decode()
    monkeypatch.setattr(taap, "_load_private_key", lambda: pem)
    for name, value in {
        "TMOBILE_ENV": "pit",
        "TMOBILE_BASE_URL": BASE_URL,
        "TMOBILE_TOKEN_URL": TOKEN_URL,
        "TMOBILE_CONSUMER_KEY": "ck",
        "TMOBILE_CONSUMER_SECRET": "cs",
        "TMOBILE_PARTNER_ID": "128",
        "TMOBILE_SENDER_ID": "128",
        "TMOBILE_ACCOUNT_ID": "",          # not available yet — generated by activation
        "TMOBILE_PRODUCT_ID": "PID-ENV",   # legacy flat field — unused by the nested body
        "TMOBILE_MARKET_ZIP": "30338",
        "TMOBILE_LANGUAGE": "ENGL",
        "TMOBILE_BASE_PRODUCT_ID": "",     # blank -> PIT constant "Infatrac Internet Access Plan"
        "TMOBILE_WPS": "",                 # blank -> PIT constant "00011586"
        "TMOBILE_CALLBACK_LOCATION": "https://pit-api.example.com/tmobile/wholesale/callback",
        # Live PIT calls authorized in the fixture so the send-path tests exercise
        # the wire request; the disabled-by-default behavior has its own test.
        "TMOBILE_PIT_LIVE_CALLS_ENABLED": "true",
    }.items():
        monkeypatch.setattr(f"app.config.settings.{name}", value)


def _mock_token_and_activate():
    respx.post(TOKEN_URL).mock(return_value=httpx.Response(
        200, json={"access_token": "tok", "expires_in": 3600}))
    respx.post(ACTIVATE_URL).mock(return_value=httpx.Response(
        200, json={"status": "accepted"}))


def _activate_request(path: str = ACTIVATE_PATH):
    for call in respx.calls:
        if call.request.url.path == path:
            return call.request
    raise AssertionError("activation request was not sent")


# ── activate_subscriber payload / headers ────────────────────────────
class TestActivateSubscriber:
    @respx.mock
    @pytest.mark.asyncio
    async def test_payload_has_no_accountid_or_msisdn(self, tmobile_env):
        _mock_token_and_activate()
        client = taap.TMobileTAAPClient()
        await client.activate_subscriber(
            iccid="8901240204219433645", market_zip="30346",
            callback_location="https://cb.example/hook")
        await client.close()

        req = _activate_request()
        body = json.loads(req.content)
        # Nested baseProduct body — exactly T-Mobile's required shape.
        assert body == {
            "iccid": "8901240204219433645",
            "marketZip": "30346",
            "language": "ENGL",
            "baseProduct": {
                "baseProductId": "Infatrac Internet Access Plan",
                "wps": "00011586",
                "product": [
                    {"ProductID": "NOROAM", "isBaseProduct": False, "action": "ADD"},
                ],
            },
        }
        assert "accountId" not in body
        assert "msisdn" not in body
        assert req.headers["call-back-location"] == "https://cb.example/hook"
        # Infatrac sender/partner = 128 (applied by _request from env).
        # T-Mobile requires the lowercase header names "sender-id" / "partner-id".
        assert req.headers["sender-id"] == "128"
        assert req.headers["partner-id"] == "128"
        # No account header while account ID is unknown.
        assert "X-Account-Id" not in req.headers

